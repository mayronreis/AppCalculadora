public class Calc {

    /*Declaração dos métodos*/
    public double soma(int num1, int num2) {
        return num1 + num2;
    }
    public double subt(int num1, int num2){
        return num1 - num2;
    }
    public double div(int num1,int num2){
        return num1 / num2;
    }
    public double mult(int num1, int num2){ return num1 * num2; }

}

